type Dict = typeof dictionary
type DeepPath<T, P extends string = ""> = T extends { es: string; en: string }
  ? P
  : {
      [K in keyof T]: K extends string ? DeepPath<T[K], P extends "" ? K : `${P}.${K}`> : never
    }[keyof T]

export type DictKey = DeepPath<Dict>

export function translate(key: DictKey, lang: Language): string {
  const parts = key.split(".")
  let node: unknown = dictionary
  for (const part of parts) {
    if (typeof node === "object" && node !== null && part in node) {
      node = (node as Record<string, unknown>)[part]
    } else {
      return key
    }
  }
  if (typeof node === "object" && node !== null && lang in node) {
    return (node as Record<string, string>)[lang]
  }
  return key
}
